USE SCHEMA ANALYTICS_SCHEMA;


CREATE OR REPLACE TABLE SALES_TREND_ANALYTICS AS
SELECT
    ORDER_DATE,
    COUNT(ORDER_NUMBER) AS TOTAL_ORDERS,
    SUM(TOTAL_REVENUE) AS TOTAL_SALES,
    SUM(PROFIT) AS TOTAL_PROFIT,
    AVG(PROFIT_MARGIN_PERCENT) AS AVG_PROFIT_MARGIN
FROM SALESPULSE360_DB.STAGING_SCHEMA.STG_SALES_ORDERS
GROUP BY ORDER_DATE
ORDER BY ORDER_DATE;

SELECT *
FROM SALES_TREND_ANALYTICS
LIMIT 10;


CREATE OR REPLACE TABLE MONTHLY_SALES_TRENDS AS
SELECT
    YEAR(ORDER_DATE) AS SALES_YEAR,
    MONTH(ORDER_DATE) AS SALES_MONTH,
    SUM(TOTAL_REVENUE) AS MONTHLY_REVENUE,
    SUM(PROFIT) AS MONTHLY_PROFIT,
    COUNT(ORDER_NUMBER) AS TOTAL_ORDERS
FROM SALESPULSE360_DB.STAGING_SCHEMA.STG_SALES_ORDERS
GROUP BY
    YEAR(ORDER_DATE),
    MONTH(ORDER_DATE)
ORDER BY
    SALES_YEAR,
    SALES_MONTH;

SELECT *
FROM MONTHLY_SALES_TRENDS
LIMIT 10;


CREATE OR REPLACE TABLE WAREHOUSE_PERFORMANCE_ANALYTICS AS
SELECT
    WAREHOUSE_CODE,
    COUNT(ORDER_NUMBER) AS TOTAL_ORDERS,
    SUM(TOTAL_REVENUE) AS TOTAL_REVENUE,
    SUM(PROFIT) AS TOTAL_PROFIT,
    AVG(DELIVERY_DAYS) AS AVG_DELIVERY_DAYS,
    MAX(DELIVERY_DAYS) AS MAX_DELIVERY_DAYS,
    MIN(DELIVERY_DAYS) AS MIN_DELIVERY_DAYS,
    AVG(PROFIT_MARGIN_PERCENT) AS AVG_PROFIT_MARGIN
FROM SALES_ANALYTICS_MASTER
GROUP BY WAREHOUSE_CODE
ORDER BY TOTAL_REVENUE DESC;

SELECT *
FROM WAREHOUSE_PERFORMANCE_ANALYTICS
LIMIT 10;


