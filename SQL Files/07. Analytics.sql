CREATE OR REPLACE TABLE EXECUTIVE_KPI_SUMMARY AS
SELECT
    COUNT(DISTINCT ORDER_NUMBER) AS TOTAL_ORDERS,
    SUM(ORDER_QUANTITY) AS TOTAL_QUANTITY_SOLD,
    SUM(TOTAL_REVENUE) AS TOTAL_SALES,
    SUM(TOTAL_UNIT_COST) AS TOTAL_COST,
    SUM(PROFIT) AS TOTAL_PROFIT,
    AVG(UNIT_PRICE) AS AVG_UNIT_PRICE,
    AVG(PROFIT_MARGIN_PERCENT) AS AVG_PROFIT_MARGIN,
    AVG(DELIVERY_DAYS) AS AVG_DELIVERY_DAYS,
    COUNT(DISTINCT CUSTOMER_NAME) AS TOTAL_CUSTOMERS,
    COUNT(DISTINCT PRODUCT_NAME) AS TOTAL_PRODUCTS,
    COUNT(DISTINCT CITY) AS TOTAL_CITIES
FROM SALES_ANALYTICS_MASTER;

SELECT*
FROM EXECUTIVE_KPI_SUMMARY
LIMIT 10;